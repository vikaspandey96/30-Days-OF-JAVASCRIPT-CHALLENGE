// Task 9: Use object.key and object.values methods to log all the keys and values of the book object. 
const book={
    title:'OS',
    author:'RAm',
    year:2020
}
console.log("all keys are:"   ,Object.keys(book));
console.log("all values are:", Object.values(book));

// output:all keys are: ['title', 'author', 'year']
// all values are:['OS', 'RAm', 2020]
