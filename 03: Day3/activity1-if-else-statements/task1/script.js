// task 1:Write a program to check if a number is positive, negative, or zero and log the result to the console
let num=0 
if(num>0){
    console.log(`This number ${num} is positive`);
}else if(num<0){
    console.log(`this number ${num} is negative`);
}else{
    console.log(`This number ${num} is zero`);
}


